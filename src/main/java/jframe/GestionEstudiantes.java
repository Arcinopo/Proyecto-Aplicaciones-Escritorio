    public void setDetEstudianteTabla(){
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");

            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/nueva_biblioteca","root","");
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("SELECT * FROM det_estudiantes");
            
            while(rs.next()){
                String idEstudiante1 = rs.getString("idEstudiante");
                String nombreEstudiante = rs.getString("nombre");
                String carreraEstudiante = rs.getString("carrera");
                
                Object [] obj = {idEstudiante1, nombreEstudiante,carreraEstudiante};
                modelo = (DefaultTableModel)tbEstudiantes.getModel();
                
                modelo.addRow(obj);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
    
    
    public boolean agregarEstudiante(){
        idEstudiante = Integer.parseInt(txtIdEstudiante.getText());
        nombre = txtNombre.getText();
        carrera = cbCarrera.getSelectedItem().toString();
        boolean valor = false;
        try {
            Connection con = DBConnection.getConnection();
            
            String sql = "INSERT INTO det_estudiantes values (?,?,?)";
            PreparedStatement pst = con.prepareStatement(sql);
            pst.setInt(1,idEstudiante);
            pst.setString(2, nombre);
            pst.setString(3, carrera);
            
            int fila = pst.executeUpdate();
            
            if(fila > 0){
                valor = true;
            }else{
                valor = false;
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
        return valor;
    }
    
    
    public boolean actualizarEstudiante(){
        idEstudiante = Integer.parseInt(txtIdEstudiante.getText());
        nombre = txtNombre.getText();
        carrera = cbCarrera.getSelectedItem().toString();
        boolean valor = false;
        
        try {
            Connection con = DBConnection.getConnection();
            
            String sql = "UPDATE det_estudiantes SET nombre = ?, carrera = ? WHERE idEstudiante = ?";
            PreparedStatement pst = con.prepareStatement(sql);
            pst.setString(1, nombre);
            pst.setString(2, carrera);
            pst.setInt(3, idEstudiante);
            int fila = pst.executeUpdate();
            
            if(fila > 0){
                valor = true;
            }else{
                valor = false;
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
        return valor;
        
    }
    
    public boolean eliminarEstudiante(){
        idEstudiante = Integer.parseInt(txtIdEstudiante.getText());
        boolean valor = false;
        
        try {
            Connection con = DBConnection.getConnection();
            
            String sql = "DELETE FROM det_estudiantes WHERE idEstudiante = ?";
            PreparedStatement pst = con.prepareStatement(sql);
            pst.setInt(1, idEstudiante);
            int fila = pst.executeUpdate();
            
            if(fila > 0){
                valor = true;
            }else{
                valor = false;
            }
        } catch (Exception e) { 
            e.printStackTrace();
        }
        return valor;
        
    }
    
    public void limpiarTabla(){
        DefaultTableModel modelo = (DefaultTableModel) tbEstudiantes.getModel();
        modelo.setRowCount(0);
    }
